import java.io.*;

class OperIncDec {

public static void main(String[] args) {

int i = 2, j = 1;

System.out.println("Operador Incremental: ");
System.out.println(" para i = 1 -> " + " i ++ -> i = i + 1 = " + i);
System.out.println("Operador Decremental: ");
System.out.println(" para j = 1 -> " + " j -- -> j = j - 1 = " + j);

} }


